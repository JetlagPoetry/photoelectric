package entity.materials;

public class Materials {
	private int Materials_id;
	private String Materials_name;
	private String Materials_notes;
	private int Materials_minPur;
	private int Materials_isLack;
	private int Materials_remains;
	private int Materials_allowed;
	public int getMaterials_id() {
		return Materials_id;
	}
	public void setMaterials_id(int materials_id) {
		Materials_id = materials_id;
	}
	public String getMaterials_name() {
		return Materials_name;
	}
	public void setMaterials_name(String materials_name) {
		Materials_name = materials_name;
	}
	public String getMaterials_notes() {
		return Materials_notes;
	}
	public void setMaterials_notes(String materials_notes) {
		Materials_notes = materials_notes;
	}
	public int getMaterials_minPur() {
		return Materials_minPur;
	}
	public void setMaterials_minPur(int materials_minPur) {
		Materials_minPur = materials_minPur;
	}
	public int getMaterials_isLack() {
		return Materials_isLack;
	}
	public void setMaterials_isLack(int materials_isLack) {
		Materials_isLack = materials_isLack;
	}
	public int getMaterials_remains() {
		return Materials_remains;
	}
	public void setMaterials_remains(int materials_remains) {
		Materials_remains = materials_remains;
	}
	public int getMaterials_allowed() {
		return Materials_allowed;
	}
	public void setMaterials_allowed(int materials_allowed) {
		Materials_allowed = materials_allowed;
	}
	
}

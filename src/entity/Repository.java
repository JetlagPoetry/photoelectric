package entity;

public class Repository {
	private int Repository_id;
	private String Repository_address;
	private int Repository_manageID;
	public int getRepository_id() {
		return Repository_id;
	}
	public void setRepository_id(int repository_id) {
		Repository_id = repository_id;
	}
	public String getRepository_address() {
		return Repository_address;
	}
	public void setRepository_address(String repository_address) {
		Repository_address = repository_address;
	}
	public int getRepository_manageID() {
		return Repository_manageID;
	}
	public void setRepository_manageID(int repository_manageID) {
		Repository_manageID = repository_manageID;
	}
	
}

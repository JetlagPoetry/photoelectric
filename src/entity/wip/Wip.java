package entity.wip;

public class Wip {
	private int Wip_id;
	private String Wip_name;
	private String Wip_notes;
	private int Wip_remains;
	private int Wip_allowed;
	private int Wip_isLack;
	public int getWip_id() {
		return Wip_id;
	}
	public void setWip_id(int wip_id) {
		Wip_id = wip_id;
	}
	public String getWip_name() {
		return Wip_name;
	}
	public void setWip_name(String wip_name) {
		Wip_name = wip_name;
	}
	public String getWip_notes() {
		return Wip_notes;
	}
	public void setWip_notes(String wip_notes) {
		Wip_notes = wip_notes;
	}
	public int getWip_remains() {
		return Wip_remains;
	}
	public void setWip_remains(int wip_remains) {
		Wip_remains = wip_remains;
	}
	public int getWip_allowed() {
		return Wip_allowed;
	}
	public void setWip_allowed(int wip_allowed) {
		Wip_allowed = wip_allowed;
	}
	public int getWip_isLack() {
		return Wip_isLack;
	}
	public void setWip_isLack(int wip_isLack) {
		Wip_isLack = wip_isLack;
	}
	

}

package entity.staff;

public class Staff {
	private int Staff_id;
	private String Staff_name;
	private String Staff_gender;
	private String Staff_title;
	private String Staff_contact;
	private int Staff_priority;
	private int Staff_department;
	private int Staff_station;
	public int getStaff_id() {
		return Staff_id;
	}
	public void setStaff_id(int staff_id) {
		Staff_id = staff_id;
	}
	public String getStaff_name() {
		return Staff_name;
	}
	public void setStaff_name(String staff_name) {
		Staff_name = staff_name;
	}
	public String getStaff_gender() {
		return Staff_gender;
	}
	public void setStaff_gender(String staff_gender) {
		Staff_gender = staff_gender;
	}
	public String getStaff_title() {
		return Staff_title;
	}
	public void setStaff_title(String staff_title) {
		Staff_title = staff_title;
	}
	public String getStaff_contact() {
		return Staff_contact;
	}
	public void setStaff_contact(String staff_contact) {
		Staff_contact = staff_contact;
	}
	public int getStaff_priority() {
		return Staff_priority;
	}
	public void setStaff_priority(int staff_priority) {
		Staff_priority = staff_priority;
	}
	public int getStaff_department() {
		return Staff_department;
	}
	public void setStaff_department(int staff_department) {
		Staff_department = staff_department;
	}
	public int getStaff_station() {
		return Staff_station;
	}
	public void setStaff_station(int staff_station) {
		Staff_station = staff_station;
	}
	
}
